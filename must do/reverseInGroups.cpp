class Solution{
public:
    //Function to reverse every sub-array group of size k.
    void reverseInGroups(vector<long long>& arr, int n, int k){
        // code here
        int i=0,j,temp,next;
        while(i<n)
        {
            j = i+k-1;
            if(j>=n)
                j = n-1;
            next = i+k;
            while(i<j)
            {
                swap(arr[i],arr[j]);
                i++;
                j--;
            }
            i = next;
        }
    }
};

